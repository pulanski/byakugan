/// The **name** of the executable from the command-line.
pub const EXE_NAME: &str = env!("CARGO_PKG_NAME");

/// The **version** of the executable from the command-line.
pub const EXE_VERSION: &str = env!("CARGO_PKG_VERSION");

/// The **author** of the executable from the command-line.
pub const EXE_AUTHOR: &str = env!("CARGO_PKG_AUTHORS");

/// The **about** description of the executable from the command-line.
pub const EXE_ABOUT: &str = env!("CARGO_PKG_DESCRIPTION");
